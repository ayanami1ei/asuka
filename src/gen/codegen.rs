use crate::grammar::ir::*;

/// Generate Rust source code from a parsed GrammarFile.
pub fn generate(gf: &GrammarFile) -> String {
    let mut out = String::new();
    out.push_str("// This file is @generated by asuka build.rs — DO NOT EDIT\n\n");
    out.push_str("#![allow(non_camel_case_types, unused)]\n\n");

    generate_span(&mut out);
    generate_tokens(gf, &mut out);
    generate_ast(gf, &mut out);
    generate_hir(gf, &mut out);
    generate_parser(gf, &mut out);
    generate_visitor(gf, &mut out);

    out
}

fn generate_span(out: &mut String) {
    out.push_str("#[derive(Debug, Clone, Copy)]\n");
    out.push_str("pub struct Span {\n");
    out.push_str("    pub start: usize,\n");
    out.push_str("    pub end: usize,\n");
    out.push_str("    pub start_line: u32,\n");
    out.push_str("    pub start_col: u32,\n");
    out.push_str("    pub end_line: u32,\n");
    out.push_str("    pub end_col: u32,\n");
    out.push_str("}\n\n");

    out.push_str("impl Span {\n");
    out.push_str("    pub fn default() -> Self {\n");
    out.push_str("        Self { start: 0, end: 0, start_line: 0, start_col: 0, end_line: 0, end_col: 0 }\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");
}

fn generate_tokens(gf: &GrammarFile, out: &mut String) {
    out.push_str("#[derive(Debug, Clone, PartialEq)]\n");
    out.push_str("pub enum TokenKind {\n");

    // Keywords
    if let Some(lex) = &gf.lexer {
        for kw in &lex.keywords {
            let name = keyword_token_name(kw);
            out.push_str(&format!("    {},\n", name));
        }
        // Operators
        for op in &lex.operators {
            let name = operator_token_name(&op.symbol);
            out.push_str(&format!("    {},\n", name));
        }
        // Punctuation
        for p in &lex.punctuation {
            let name = punct_token_name(p);
            out.push_str(&format!("    {},\n", name));
        }
        // Literal tokens from @tokens
        for t in &gf.tokens {
            out.push_str(&format!("    {},\n", t.name.as_str()));
        }
    }
    out.push_str("    EOF,\n");
    out.push_str("}\n\n");

    // Token struct
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str("pub struct Token {\n");
    out.push_str("    pub kind: TokenKind,\n");
    out.push_str("    pub span: Span,\n");
    out.push_str("    pub value: String,\n");
    out.push_str("}\n\n");

    // Lexer stub
    out.push_str("pub struct Lexer {\n");
    out.push_str("    chars: Vec<char>,\n");
    out.push_str("    pos: usize,\n");
    out.push_str("    line: u32,\n");
    out.push_str("    col: u32,\n");
    out.push_str("}\n\n");

    out.push_str("impl Lexer {\n");
    out.push_str("    pub fn new(source: &str) -> Self {\n");
    out.push_str("        Self { chars: source.chars().collect(), pos: 0, line: 1, col: 1 }\n");
    out.push_str("    }\n\n");
    out.push_str("    pub fn tokenize(&mut self) -> Vec<Token> {\n");
    out.push_str("        let mut tokens = Vec::new();\n");
    out.push_str("        loop {\n");
    out.push_str("            self.skip_ws();\n");
    out.push_str("            if self.pos >= self.chars.len() { break; }\n");
    out.push_str("            let c = self.chars[self.pos];\n");
    out.push_str("            match c {\n");

    // String literals
    out.push_str("                '\"' => { tokens.push(self.read_string()); }\n");
    // Number literals
    out.push_str("                c if c.is_ascii_digit() => { tokens.push(self.read_number()); }\n");
    // Identifier/keyword
    out.push_str("                c if c.is_alphabetic() || c == '_' => { tokens.push(self.read_ident_or_keyword()); }\n");

    // Operators and punctuation
    if let Some(lex) = &gf.lexer {
        let all_syms: Vec<&str> = lex.operators.iter().map(|o| o.symbol.as_str())
            .chain(lex.punctuation.iter().map(|s| s.as_str()))
            .collect();
        // Sort by length descending so multi-char ops match first
        let mut sorted = all_syms.clone();
        sorted.sort_by_key(|s| std::cmp::Reverse(s.len()));
        for sym in &sorted {
            let ch = sym.chars().next().unwrap();
            if ch.is_alphabetic() || ch == '_' || ch == '"' || ch.is_ascii_digit() { continue; }
            let name = operator_token_name(sym);
            out.push_str(&format!("                '{}' => tokens.push(self.read_fixed(\"{}\", TokenKind::{})),\n",
                ch, sym, name));
        }
    }
    out.push_str("                _ => { panic!(\"unexpected char '{}' at {}:{}\", c, self.line, self.col); }\n");
    out.push_str("            }\n");
    out.push_str("        }\n");
    out.push_str("        tokens\n");
    out.push_str("    }\n");

    // Lexer helper methods
    out.push_str("    fn skip_ws(&mut self) {\n");
    out.push_str("        loop {\n");
    out.push_str("            let c = self.chars.get(self.pos);\n");
    out.push_str("            match c {\n");
    out.push_str("                Some(' ' | '\\t' | '\\r') => { self.pos += 1; self.col += 1; }\n");
    out.push_str("                Some('\\n') => { self.pos += 1; self.line += 1; self.col = 1; }\n");
    out.push_str("                _ => break,\n");
    out.push_str("            }\n");
    out.push_str("        }\n");
    out.push_str("    }\n\n");
    out.push_str("    fn read_string(&mut self) -> Token { unimplemented!() }\n");
    out.push_str("    fn read_number(&mut self) -> Token { unimplemented!() }\n");
    out.push_str("    fn read_ident_or_keyword(&mut self) -> Token { unimplemented!() }\n");
    out.push_str("    fn read_fixed(&mut self, s: &str, kind: TokenKind) -> Token { unimplemented!() }\n");
    out.push_str("}\n\n");
}

fn generate_ast(gf: &GrammarFile, out: &mut String) {
    if gf.ast.is_empty() { return; }

    out.push_str("// ─── AST Nodes ───\n\n");
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str("pub enum AstNode {\n");
    for rule in &gf.ast {
        out.push_str(&format!("    {}(Box<Ast{}>),\n", rule.name.as_str(), rule.name.as_str()));
    }
    out.push_str("    Error,\n");
    out.push_str("}\n\n");

    for rule in &gf.ast {
        let name = rule.name.as_str();
        out.push_str(&format!("#[derive(Debug, Clone)]\n"));
        out.push_str(&format!("pub struct Ast{} {{\n", name));
        out.push_str(&format!("    pub span: Span,\n"));
        // Generate fields from production
        collect_rule_fields(&rule.production, out, "    pub ");
        out.push_str("}\n\n");
    }
}

fn generate_hir(gf: &GrammarFile, out: &mut String) {
    if gf.hir.is_empty() { return; }

    out.push_str("// ─── HIR Nodes ───\n\n");
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str("pub enum HirNode {\n");
    for rule in &gf.hir {
        out.push_str(&format!("    {}(Box<Hir{}>),\n", rule.name.as_str(), rule.name.as_str()));
    }
    out.push_str("}\n\n");

    for rule in &gf.hir {
        let name = &rule.name.as_str();
        out.push_str(&format!("#[derive(Debug, Clone)]\n"));
        out.push_str(&format!("pub struct Hir{} {{\n", name));
        out.push_str(&format!("    pub span: Span,\n"));
        collect_rule_fields(&rule.production, out, "    pub ");
        out.push_str("}\n\n");
    }
}

fn generate_parser(gf: &GrammarFile, out: &mut String) {
    if gf.ast.is_empty() { return; }

    out.push_str("// ─── Parser ───\n\n");
    out.push_str("pub struct Parser {\n");
    out.push_str("    tokens: Vec<Token>,\n");
    out.push_str("    pos: usize,\n");
    out.push_str("}\n\n");

    out.push_str("impl Parser {\n");
    out.push_str("    pub fn new(tokens: Vec<Token>) -> Self {\n");
    out.push_str("        Self { tokens, pos: 0 }\n");
    out.push_str("    }\n\n");

    // Generate parse methods for each AST rule
    for rule in &gf.ast {
        let name = rule.name.as_str();
        out.push_str(&format!("    pub fn parse_{}(&mut self) -> Result<AstNode, String> {{\n",
            to_snake(&name)));
        out.push_str(&format!("        let start = self.token().span;\n"));
        out.push_str(&format!("        let node = Ast{} {{ span: Span::default(), /* TODO */ }};\n", name));
        out.push_str(&format!("        Ok(AstNode::{}(Box::new(node)))\n", name));
        out.push_str("    }\n\n");
    }

    // Helper methods
    out.push_str("    fn token(&self) -> &Token {\n");
    out.push_str("        &self.tokens[self.pos]\n");
    out.push_str("    }\n\n");
    out.push_str("    fn advance(&mut self) { self.pos += 1; }\n\n");
    out.push_str("    fn expect(&mut self, kind: TokenKind) -> Result<(), String> {\n");
    out.push_str("        if self.token().kind == kind { self.advance(); Ok(()) }\n");
    out.push_str("        else { Err(format!(\"expected {:?} at line {}\", kind, self.token().span.start_line)) }\n");
    out.push_str("    }\n\n");
    out.push_str("}\n\n");
}

fn generate_visitor(gf: &GrammarFile, out: &mut String) {
    if gf.ast.is_empty() && gf.hir.is_empty() { return; }

    out.push_str("// ─── Visitor ───\n\n");

    // AST Visitor
    if !gf.ast.is_empty() {
        out.push_str("pub trait AstVisitor<T> {\n");
        for rule in &gf.ast {
            let name = &rule.name.as_str();
            out.push_str(&format!("    fn visit_{}(&mut self, node: &Ast{}) -> T;\n",
                to_snake(name), name));
        }
        out.push_str("}\n\n");
    }
}

// ─── Helpers ───

fn collect_rule_fields(prod: &Production, out: &mut String, prefix: &str) {
    match prod {
        Production::Seq(symbols) => {
            for (i, sym) in symbols.iter().enumerate() {
                match &sym.kind {
                    ProductionSymbolKind::NonTerm(name) => {
                        out.push_str(&format!("{}pub {}: Box<AstNode>,\n", prefix,
                            to_snake(&name.as_str())));
                    }
                    ProductionSymbolKind::Terminal(_) => {
                        out.push_str(&format!("{}pub token_{}: Token,\n", prefix, i));
                    }
                    ProductionSymbolKind::Literal(_) => {}
                }
            }
        }
        _ => {
            out.push_str(&format!("{}pub children: Vec<AstNode>,\n", prefix));
        }
    }
}

fn keyword_token_name(kw: &str) -> String {
    match kw {
        "fn" => "KwFn".into(),
        "let" => "KwLet".into(),
        "return" => "KwReturn".into(),
        "if" => "KwIf".into(),
        "else" => "KwElse".into(),
        "true" => "KwTrue".into(),
        "false" => "KwFalse".into(),
        "while" => "KwWhile".into(),
        "for" => "KwFor".into(),
        "in" => "KwIn_".into(),
        "match" => "KwMatch".into(),
        "enum" => "KwEnum".into(),
        "struct" => "KwStruct".into(),
        "impl" => "KwImpl".into(),
        "pub" => "KwPub".into(),
        "shared" => "KwShared".into(),
        "unique" => "KwUnique".into(),
        "weak" => "KwWeak".into(),
        "extern" => "KwExtern".into(),
        "import" => "KwImport".into(),
        "as" => "KwAs".into(),
        "break" => "KwBreak".into(),
        "continue" => "KwContinue".into(),
        _ => {
            let mut s = String::from("Kw");
            for ch in kw.chars() {
                if ch.is_alphabetic() { s.push(ch.to_ascii_uppercase()); }
            }
            s
        }
    }
}

fn operator_token_name(sym: &str) -> String {
    if sym.is_empty() { return "Sym".into(); }
    let mut out = String::new();
    for c in sym.chars() {
        out.push_str(match c {
            '+' => "Plus",
            '-' => "Minus",
            '*' => "Star",
            '/' => "Slash",
            '=' => "Eq",
            '!' => "Bang",
            '<' => "Lt",
            '>' => "Gt",
            '&' => "And",
            '|' => "Or",
            '^' => "Caret",
            '%' => "Percent",
            '~' => "Tilde",
            '?' => "Question",
            ':' => "Colon",
            '.' => "Dot",
            ',' => "Comma",
            ';' => "Semi",
            '(' => "LParen",
            ')' => "RParen",
            '{' => "LBrace",
            '}' => "RBrace",
            '[' => "LBracket",
            ']' => "RBracket",
            '_' => "Underscore",
            '@' => "At",
            '#' => "Hash",
            '$' => "Dollar",
            '"' => "Quote",
            '\'' => "Apos",
            '\\' => "Backslash",
            '`' => "Backtick",
            ' ' => "Space",
            '\t' => "Tab",
            '\n' => "Newline",
            _ => "Other",
        });
    }
    out
}

fn punct_token_name(p: &str) -> String {
    operator_token_name(p)
}

fn to_snake(name: &str) -> String {
    let mut out = String::new();
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() && i > 0 {
            out.push('_');
        }
        out.push(ch.to_ascii_lowercase());
    }
    out
}
